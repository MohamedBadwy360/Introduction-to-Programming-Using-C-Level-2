#include <iostream>

using namespace std;


int main()
{
    std::cout << "And Result is: " << (12 && 25) << endl;   // = 1
    std::cout << "Bitwise And Result is: " << (12 & 25) << endl;    // = 8
    std::cout << "Or Result is: " << (12 || 25) << endl;   // = 1
    std::cout << "Bitwise Or Result is: " << (12 | 25) << endl;    // = 1
}

